#include "minishell.h"

void	print_tab(int *tab, int n)
{
	int	i;

	i = 0;
	if (!tab || n == 0)
		return ;
	while (i < n)
	{
		printf("tab[%d] = %d\n", i, tab[i]);
		i++;
	}
	printf("-----------------------------------------------");
}
